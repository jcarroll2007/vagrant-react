#! /usr/bin/bash

echo "mounting node_modules"
src=/srv/node_modules
target=/vagrant/node_modules

if [[ ! -d $src ]] ; then
  mkdir -p $src
  chmod 777 $src
fi

if [[ ! -d $target ]] ; then
  mkdir -p $target
fi

if ! mountpoint -q $target ; then
  # ensure mountpoint exists
  mkdir -p $target
  mount --bind $src $target
fi